package leetcode

// @lc code=start
func findMinII(nums []int) int {
	low := 0
	high := len(nums) - 1
	for low < high {
		pivot := low + (high-low)/2
		if nums[pivot] < nums[high] {
			high = pivot
		} else if nums[pivot] > nums[high] {
			low = pivot + 1
		} else {
			high--
		}
	}
	return nums[low]
}

